        // POST: Rates/Edit/5
        // To protect from overposting attacks, please enable the specific properties you want to bind to, for more details see http://go.microsoft.com/fwlink/?LinkId=317598.
        [HttpPost]
        //[ValidateAntiForgeryToken]
        public ActionResult Edit([Bind(Include = "Id,LocalCurrency，LocalCurrCode,ForeignCurrency,ForeignCurrCode,Year,Month,RecRate,PayRate,Description,Status,OperatingPoint,ADDWHO,ADDTS,EDITWHO,EDITTS")] Rate rate)
        {
            if (ModelState.IsValid)
            {
                rate.ObjectState = ObjectState.Modified;
                _rateService.Update(rate);
                _unitOfWork.SaveChanges();
                if (Request.IsAjaxRequest())
                {
                    return Json(new { Success = true }, JsonRequestBehavior.AllowGet);
                }
                DisplaySuccessMessage("Has update a Rate record");
                return RedirectToAction("Index");
